// Function to encode a number to a string
export function encode(number: number) {
  // Convert the number to a base64-like string representation
  const charset =
    "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789-_";
  let encoded = "";
  while (number > 0) {
    const remainder = number % 64;
    encoded = charset[remainder] + encoded;
    number = Math.floor(number / 64);
  }
  return encoded || "A"; // Return 'A' for the number 0
}

// Function to decode an encoded string back to a number
export function decode(encoded: string) {
  const charset =
    "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789-_";
  let number = 0;
  for (let i = 0; i < encoded.length; i++) {
    const index = charset.indexOf(encoded[i]);
    if (index === -1) {
      throw new Error(`Invalid character '${encoded[i]}' in encoded string`);
    }
    number = number * 64 + index;
  }
  return number;
}

export const generateSecurePassword = (): string => {
  return crypto
    .randomBytes(12) // 12 bytes = 24 characters
    .toString("base64") // Convert to a base64 string for better randomness
    .replace(/[^a-zA-Z0-9]/g, "") // Remove non-alphanumeric characters (optional)
    .slice(0, 16); // Trim to 16 characters
};
